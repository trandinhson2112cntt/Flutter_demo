import 'package:flutter/material.dart';
import 'package:flutter_demo/AccountTab.dart';
import 'package:flutter_demo/IssueTab.dart';
import 'package:flutter_demo/NotificationTab.dart';
import 'package:flutter_demo/ProjectTab.dart';
import 'package:flutter_demo/home_screen.dart';
import 'package:flutter_demo/project_detail.dart';

class TabNavigatorRoutes {
  static const String root = '/';
  static const String detail = '/detail';
}

class TabNavigator extends StatelessWidget {
  TabNavigator({this.navigatorKey, this.tabItem});
  final GlobalKey<NavigatorState> navigatorKey;
  final TabItem tabItem;

  Map<String, WidgetBuilder> _routeBuilders(BuildContext context,
      {int materialIndex: 500}) {
    return {
      TabNavigatorRoutes.root: (context) => ProjectTab(
      ),
      TabNavigatorRoutes.detail: (context) => ProjectDetail(
      ),
    };
  }

  @override
  Widget build(BuildContext context) {
    var routeBuilders = _routeBuilders(context);

    return Navigator(
        key: navigatorKey,
        initialRoute: TabNavigatorRoutes.root,
        onGenerateRoute: (routeSettings) {
          return MaterialPageRoute(
              builder: (context) => routeBuilders[routeSettings.name](context));
        });
  }
}

